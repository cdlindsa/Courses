    CONSTRAINT "ADR_ID_FK" FOREIGN KEY (ADR_ID) REFERENCES ADR (ADR_ID),
);

CREATE TABLE DRUGS_AE (
    DrugID INTEGER(10),
    ReportID INTEGER(10),
    CONSTRAINT "DrugID_FK" FOREIGN KEY (DrugID) REFERENCES DRUG (DrugID),
    CONSTRAINT "ReportID_FK" FOREIGN KEY (ReportID) REFERENCES AE_REPORT (ReportID),
);

CREATE TABLE SYNONYM (
